package Chapter03;

public class Operater02 {
    public static void main(String[] args) {
        int c = 10, d = 20;

        System.out.println("c == d: " + (c == d)); // 같음
        System.out.println("c != d: " + (c != d)); // 다름
        System.out.println("c > d: " + (c > d)); // 큼
        System.out.println("c < d: " + (c < d)); // 작음
    }
}
