package Chapter03;

public class Operater02 {
    public static void main(String[] args) {
        int a = 10, b = 20;

        System.out.println("c == d: " + (a == b)); // 같음
        System.out.println("c != d: " + (a != b)); // 다름
        System.out.println("c > d: " + (a > b)); // 큼
        System.out.println("c < d: " + (a < b)); // 작음
    }
}
